; ============================================================================
; PALRAM2.ASM - Scanline Palette Demo (Intermediate) During HSYNC
; Demonstrates per-scanline palette RAM manipulation with multiple modes
; Written for NASM - NEC V40 (80186 compatible) @ 8 MHz
; By Retro Erik - 2026
;
; Target: Olivetti PC1 with Yamaha V6355D video controller
; Video Mode: CGA 160x200x16 (Hidden mode)
;
; DISTINGUISHES THIS VERSION:
;   - 6 different gradient modes (Rainbow, RGB Cube, Sunset, Ocean, Fire, Gray)
;   - Keyboard-selectable palettes (press 1-6)
;   - Mid-range complexity for learning
;   - ~622 lines of code
;
; ============================================================================
; TECHNIQUE: Per-scanline palette RAM writes
; ============================================================================
;
; Palette updates are synchronized to the HSYNC edge each scanline.
;
; Each scanline, we change palette entry 0's RGB values (3 OUTs).
; Screen is filled with color index 0, so each line appears different.
;
; PALETTE MODES (press 1-6 to switch):
;   1: Smooth Rainbow + Grayscale (default)
;   2: RGB Cube Snake (all 200 unique from 512)
;   3: Warm Sunset Gradient
;   4: Cool Ocean Gradient
;   5: Fire Gradient
;   6: All Grayscale (black to white to black)
;
; Controls:
;   1-6 - Select palette mode
;   ESC - Exit to DOS
; ============================================================================

[BITS 16]
[ORG 0x100]

; ============================================================================
; Constants
; ============================================================================

PORT_MODE       equ 0x3D8
PORT_STATUS     equ 0x3DA
PORT_PAL_ADDR   equ 0x3DD
PORT_PAL_DATA   equ 0x3DE
VIDEO_SEG       equ 0xB000
SCREEN_HEIGHT   equ 200
NUM_PALETTES    equ 7

; ============================================================================
; Main Program
; ============================================================================
main:
    ; Initialize with palette 1
    mov byte [current_palette], 0
    call load_current_palette
    
    call enable_graphics_mode
    call clear_screen
    
.main_loop:
    call wait_vblank
    call render_scanlines
    call check_keyboard
    
    cmp al, 0xFF            ; Exit flag?
    jne .main_loop
    
    ; Cleanup and exit
    mov al, 0x40
    out PORT_PAL_ADDR, al
    xor al, al
    out PORT_PAL_DATA, al
    out PORT_PAL_DATA, al
    
    mov ax, 0x0003
    int 0x10
    mov ax, 0x4C00
    int 0x21

; ============================================================================
; check_keyboard - Handle palette switching
; Returns: AL = 0xFF if exit, else 0
; ============================================================================
check_keyboard:
    push bx
    
    mov ah, 0x01
    int 0x16
    jz .no_key              ; No key available
    
    mov ah, 0x00
    int 0x16                ; AH = scancode, AL = ASCII
    
    ; Check ESC
    cmp ah, 0x01
    jne .not_esc
    mov al, 0xFF
    jmp .done
    
.not_esc:
    ; Check 1-7 keys (ASCII '1' to '7')
    cmp al, '1'
    jb .no_key
    cmp al, '7'
    ja .no_key
    
    ; Convert ASCII to palette index (0-5)
    sub al, '1'
    cmp al, [current_palette]
    je .no_key              ; Same palette, skip
    
    mov [current_palette], al
    call load_current_palette
    
.no_key:
    xor al, al              ; Return 0 (continue)
    
.done:
    pop bx
    ret

; ============================================================================
; load_current_palette - Copy selected palette to working buffer
; ============================================================================
load_current_palette:
    push ax
    push bx
    push cx
    push si
    push di
    
    ; Get palette table address
    mov al, [current_palette]
    xor ah, ah
    shl ax, 1               ; *2 for word index
    mov bx, ax
    mov si, [palette_table + bx]
    
    ; Copy 200 entries (400 bytes) to color_table
    mov di, color_table
    mov cx, 400
.copy:
    mov al, [si]
    mov [di], al
    inc si
    inc di
    loop .copy
    
    pop di
    pop si
    pop cx
    pop bx
    pop ax
    ret

; ============================================================================
; render_scanlines - Output colors for all 200 scanlines
; ============================================================================
render_scanlines:
    push ax
    push cx
    push dx
    push si
    
    cli
    xor si, si
    mov cx, SCREEN_HEIGHT
    mov dx, PORT_STATUS
    
.scanline_loop:
.wait_low:
    in al, dx
    test al, 0x01
    jnz .wait_low
.wait_high:
    in al, dx
    test al, 0x01
    jz .wait_high
    
    mov al, 0x40
    out PORT_PAL_ADDR, al
    mov al, [color_table + si]
    out PORT_PAL_DATA, al
    mov al, [color_table + si + 1]
    out PORT_PAL_DATA, al
    
    add si, 2
    loop .scanline_loop
    
    mov al, 0x40
    out PORT_PAL_ADDR, al
    xor al, al
    out PORT_PAL_DATA, al
    out PORT_PAL_DATA, al
    
    sti
    pop si
    pop dx
    pop cx
    pop ax
    ret

; ============================================================================
; wait_vblank
; ============================================================================
wait_vblank:
    push ax
    push dx
    mov dx, PORT_STATUS
.wait_end:
    in al, dx
    test al, 0x08
    jnz .wait_end
.wait_start:
    in al, dx
    test al, 0x08
    jz .wait_start
    pop dx
    pop ax
    ret

; ============================================================================
; enable_graphics_mode
; ============================================================================
enable_graphics_mode:
    mov al, 0x4A
    out PORT_MODE, al
    ret

; ============================================================================
; clear_screen
; ============================================================================
clear_screen:
    push ax
    push cx
    push di
    push es
    mov ax, VIDEO_SEG
    mov es, ax
    xor di, di
    mov cx, 8192
    xor ax, ax
    cld
    rep stosw
    pop es
    pop di
    pop cx
    pop ax
    ret

